## 💼 Prompt 16: Upsell Recommendation Generator

**Objective:** Suggest relevant products/services to upsell.

---

### 📝 Prompt Text  
You are a sales assistant. Based on this customer’s purchase history, suggest **3 upsell items** and explain why.  

Purchase History:  
[Insert data]  

---

### 📥 Sample Input  

| Product Purchased      | Purchase Date  | Quantity |  
|-----------------------|----------------|----------|  
| Wireless Earbuds       | 2025-05-10     | 1        |  
| Smartphone Case       | 2025-05-10     | 1        |  
| Portable Charger      | 2025-06-15     | 1        |  

---

### 📤 Sample Output  

| Recommended Product    | Reason for Recommendation                                                |  
|-----------------------|--------------------------------------------------------------------------|  
| Bluetooth Headphones   | Complements earbuds; offers premium audio experience.                   |  
| Screen Protector      | Protects smartphone screen; pairs well with the case already purchased.  |  
| Power Bank with Solar | Upgraded portable charger for outdoor use; appeals to active users.     |  

---

### 🔧 Customization Notes  
- Tailor recommendations by customer demographics and preferences.  
- Incorporate seasonality and current promotions.  
- Use dynamic pricing and inventory data for real-time suggestions.  

---

### 💡 Recommended LLM / Platform  
- **GPT-4** — for personalized, context-aware upsell suggestions.  
- Integration with CRM and sales platforms for automation.
