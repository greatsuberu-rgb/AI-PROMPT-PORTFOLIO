## 📝 Prompt 15: Sentiment Analysis

**Objective:** Identify emotional tone in customer messages.

---

### 📝 Prompt Text  
You are a sentiment analyst. Classify the following text as **Positive**, **Neutral**, or **Negative**, and explain why.  

Text:  
[Insert text]  

---

### 📥 Sample Input  

| Customer Message                                  |  
|-------------------------------------------------|  
| "I’m really happy with the quick service I received!" |  

---

### 📤 Sample Output  

| Sentiment | Reasoning                                                     |  
|-----------|---------------------------------------------------------------|  
| Positive  | The message expresses satisfaction and appreciation for fast service. |  

---

### 🔧 Customization Notes  
- Support fine-grained sentiment scales (e.g., very positive to very negative).  
- Incorporate aspect-based sentiment for targeted analysis.  
- Integrate with customer feedback platforms for real-time monitoring.  

---

### 💡 Recommended LLM / Platform  
- **GPT-4** — for nuanced sentiment classification and explanation.  
- **Claude** — alternative for varied tone detection.
