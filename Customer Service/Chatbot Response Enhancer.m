## 🤖 Prompt 14: Chatbot Response Enhancer

**Objective:** Improve chatbot replies for clarity and empathy.

---

### 📝 Prompt Text  
You are a chatbot trainer. Rewrite the following bot response to be:  
- Friendly  
- Professional  
- Clear  

Original Response:  
[Insert text]  

---

### 📥 Sample Input  

| Original Response                                   |  
|----------------------------------------------------|  
| Sorry, I can’t help with that request right now.   |  

---

### 📤 Sample Output  

| Enhanced Response                                   |  
|----------------------------------------------------|  
| I’m sorry, but I’m unable to assist with that request at the moment. Please try again later or contact our support team for further help. Thank you for your understanding! |  

---

### 🔧 Customization Notes  
- Adjust tone based on target audience (formal vs casual).  
- Include multilingual support for global users.  
- Add fallback prompts or escalation suggestions.  

---

### 💡 Recommended LLM / Platform  
- **GPT-4** — for nuanced and empathetic response refinement.  
- Integration with chatbot platforms like Dialogflow or Rasa.
