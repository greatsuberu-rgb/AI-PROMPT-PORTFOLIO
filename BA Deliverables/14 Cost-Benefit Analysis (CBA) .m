# 📄 Prompt 14: Cost-Benefit Analysis (CBA)  

## 🎯 Objective  
To evaluate the **financial justification** of a project by comparing the **estimated costs** against the **anticipated benefits**.  
A **Cost-Benefit Analysis (CBA)** helps decision-makers determine whether a project should proceed by quantifying its **economic value and ROI**.  

---

## 📝 Prompt Text  
You are a **Business Analyst**. Conduct a **Cost-Benefit Analysis (CBA)** for the proposed project.  
Include the following sections:  

1. **Executive Summary** – concise overview of results.  
2. **Project Costs** – initial investment, operational costs, ongoing maintenance.  
3. **Project Benefits** – tangible (financial savings, efficiency gains) and intangible (customer satisfaction, brand reputation).  
4. **Cost-Benefit Comparison Table** – side-by-side costs vs. benefits.  
5. **ROI Calculation** – return on investment and payback period.  
6. **Sensitivity Analysis** – assess impact of changes in cost/benefit assumptions.  
7. **Recommendation** – whether to proceed based on net benefit.  

Additionally, provide:  
- A **Narrative Summary** highlighting the importance of CBA in project evaluation.  

---

## 📥 Sample Input  

**Project Idea:** Implementation of an AI-powered chatbot for customer service.  

**Context:** The company receives 50,000+ support queries monthly. Response times are long, leading to low customer satisfaction and high call center costs.  

---

## 📤 Sample Output  

### 📑 Cost-Benefit Analysis  

#### 1. Executive Summary  
The AI-powered chatbot project delivers a **net benefit of £380,000 per year**, with a payback period of **less than 2 years**. The investment is financially justified.  

#### 2. Project Costs  
- **Development & Implementation:** £200,000  
- **Annual Maintenance & Updates:** £50,000  
- **Training & Change Management:** £20,000  
**Total Year 1 Cost:** £270,000  

#### 3. Project Benefits  
- **Reduced Customer Service Costs:** £400,000/year (fewer call center staff required).  
- **Improved Customer Retention:** £150,000/year projected revenue from higher satisfaction.  
- **24/7 Availability (Intangible):** Increased customer trust and loyalty.  
- **Brand Reputation (Intangible):** Innovation positioning.  
**Total Benefits:** £550,000/year  

#### 4. Cost-Benefit Comparison Table  

| Category            | Costs (£) | Benefits (£) |
|---------------------|-----------|--------------|
| Development         | 200,000   | -            |
| Maintenance         | 50,000    | -            |
| Training            | 20,000    | -            |
| Customer Service    | -         | 400,000      |
| Retention Revenue   | -         | 150,000      |
| Intangibles         | -         | Qualitative  |
| **Total**           | 270,000   | 550,000      |

#### 5. ROI Calculation  
- **Net Benefit:** £550,000 - £270,000 = £280,000/year  
- **ROI:** (280,000 ÷ 270,000) × 100 = **104%**  
- **Payback Period:** £270,000 ÷ £280,000 ≈ **0.96 years**  

#### 6. Sensitivity Analysis  
- If customer retention revenue is 50% lower (£75,000), ROI still remains positive at 76%.  
- If costs increase by 20% (£324,000 total), ROI reduces to 70% but still viable.  

#### 7. Recommendation  
The project is **financially sound** with a strong ROI and quick payback period. Recommendation: Proceed with chatbot implementation.  

---

### 2️⃣ Narrative Explanation  

A **Cost-Benefit Analysis** ensures:  
- Projects are **financially justified** before approval.  
- Decision-makers can compare **competing initiatives**.  
- Risks of overspending on low-value projects are minimized.  
- Intangible benefits (brand, customer trust) are factored alongside hard numbers.  

Without CBA, organizations risk **pursuing projects that drain resources without clear returns**.  

---

### 🔧 Customization Notes  
- Can include **Net Present Value (NPV)** or **Internal Rate of Return (IRR)** for investment-heavy projects.  
- Intangible benefits can be rated using a **qualitative scoring model**.  
- For public sector projects, include **social and environmental impacts**.  
- Time horizon can be adjusted: short-term (1–2 years) vs. long-term (5–10 years).  

