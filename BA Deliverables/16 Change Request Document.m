# 📄 Prompt 16: Change Request Document  

## 🎯 Objective  
To formally **request, evaluate, and approve changes** to the scope, schedule, or budget of a project.  
A **Change Request Document (CRD)** ensures all modifications are **controlled, traceable, and authorized**.  

---

## 📝 Prompt Text  
You are a **Business Analyst / Project Manager**. Create a **Change Request Document** for a proposed project change.  
Include the following sections:  

1. **Change Request ID** – unique identifier.  
2. **Project Name** – project affected by the change.  
3. **Requestor Name & Role** – who is initiating the change.  
4. **Date of Request** – when the request is submitted.  
5. **Change Description** – detailed explanation of the requested change.  
6. **Reason for Change** – justification for the modification.  
7. **Impact Analysis** – effects on scope, schedule, budget, quality, and resources.  
8. **Priority / Urgency** – High / Medium / Low.  
9. **Recommended Action** – Approve / Reject / Defer.  
10. **Approval Signatures** – Project Manager, Sponsor, and other stakeholders.  

Additionally, provide:  
- A **Narrative Summary** highlighting the importance of controlled change management.  

---

## 📥 Sample Input  

**Project:** Implementation of AI-powered customer support chatbot.  

**Requested Change:** Add multi-language support for French and Spanish in chatbot responses.  

**Requestor:** Emily Davis, Customer Experience Lead  

**Date of Request:** August 10, 2025  

---

## 📤 Sample Output  

### 📑 Change Request Document  

**Change Request ID:** CR-2025-008  

**Project Name:** AI-Powered Customer Support Chatbot Implementation  

**Requestor Name & Role:** Emily Davis, Customer Experience Lead  

**Date of Request:** August 10, 2025  

**Change Description:**  
Implement **multi-language support** for French and Spanish in all chatbot interactions.  

**Reason for Change:**  
To improve **customer satisfaction** and serve international clients more effectively.  

**Impact Analysis:**  

| Impact Area       | Effect                                         |
|------------------|-----------------------------------------------|
| Scope             | Expands chatbot functionality to 2 new languages |
| Schedule          | Adds 3 weeks to development and testing timeline |
| Budget            | Additional £15,000 for translation & QA       |
| Resources         | Requires language experts and QA testers      |
| Quality           | Potential improvement in customer satisfaction |

**Priority / Urgency:** High  

**Recommended Action:** Approve with revised timeline and budget  

**Approval Signatures:**  
- Project Manager: Mark Johnson  
- Project Sponsor: Jane Smith  
- Customer Support Lead: Tom Brown  

---

### 2️⃣ Narrative Explanation  

The **Change Request Document** is essential because:  
- It ensures **all project changes are formally recorded and assessed**.  
- Helps **prevent scope creep** by evaluating impacts before approval.  
- Provides **transparency and accountability** for stakeholders.  
- Ensures **resources, timeline, and budget** are considered before implementing changes.  

Without a CRD, projects risk **uncontrolled modifications** leading to delays, cost overruns, and misaligned deliverables.  

---

### 🔧 Customization Notes  
- Can include **risk assessment** for each proposed change.  
- May integrate with **project management tools** for automatic tracking.  
- Can include **change history** for audit purposes.  
- Priority scoring can be numeric for more granular ranking of changes.  

